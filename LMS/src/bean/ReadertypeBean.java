package bean;

public class ReadertypeBean {
	private int rt_id;
	private String rt_name;
	private int rt_number;
	private int rt_time;
	public int getRt_id() {
		return rt_id;
	}
	public void setRt_id(int rt_id) {
		this.rt_id = rt_id;
	}
	public String getRt_name() {
		return rt_name;
	}
	public void setRt_name(String rt_name) {
		this.rt_name = rt_name;
	}
	public int getRt_number() {
		return rt_number;
	}
	public void setRt_number(int rt_number) {
		this.rt_number = rt_number;
	}
	public int getRt_time() {
		return rt_time;
	}
	public void setRt_time(int rt_time) {
		this.rt_time = rt_time;
	}
	
	
	
}	
